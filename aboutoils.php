      <h1 class="header--vertical-margin">Using Oils</h1>

      <p class="ptext">
        These essential oils are potent. Imagine tons of plants distilled into gallons of essential oils. Treat them respectfully!
        <br>
        <br>

        <span class="ptext-bold">Bath:</span> Relaxation, relief from colds, aches or pains can be served from bathing in essential oils. Place 10-15 drops of a blend or single oil in an adult bath and enjoy. Some users prefer to mix the oils into a cup of milk and add it to the bath for better dispersing. Add milk when you first turn the water on before it gets too hot. The milk will warm with the water and prevent curdling.
        <br>
        <br>

        <span class="ptext-bold">Blends:</span> Rashes, burns, skin diseases, varicose veins, upset tummies, torn ligaments, tired, inflamed or injured muscles, injured bones, skin and organ support and illnesses can all be aided using an essential oil or blend in a carrier oil.
        <br>
        <br>

        <span class="ptext-bold">Compress:</span> Oils can be applied externally and then compressed with wet or dry heat for 10-20 minutes to help the oils sink deep into tissue, muscle and bone.
        <br>
        <br>

        <span class="ptext-bold">Diffuser:</span> Diffusers use pressure to put essential oils in the air without heat. They are absorbed through breathing. Diffusers are helpful to clear the air during outbreaks. They are very helpful aids for colds, sinus infection, sore throat, asthma, bronchitis, lung and respiratory issues. They disinfect the environment for mold, fungus, and airborne illnesses. Essential oils may also be diffused to create calm, peaceful environment, improve concentration and enhance meditation. For example, Lemon and Rosemary increase concentration. Peppermint or Orange increase alertness and aid a sense of well -being. Lavender, Ylang Ylang, Clary Sage or Bitter Orange help clear and balance the emotions.
        <br>
        <br>

        <span class="ptext-bold">When dealing with lung issues, inhale lightly and step away to ensure there is no sensitivity or reaction to the oil before using.</span>
        <br>
        <br>

        <span class="ptext-bold">DO NOT PUT CARRIER OILS IN A DIFFUSER.</span> They will clog the diffuser.
        <br>
        <br>

        <span class="ptext-italic">Don’t have a diffuser? Place a drop of essential oil on a cotton ball or plain Kleenex and breathe in.</span>
        <br>
        <br>

        <span class="ptext-bold">Footbath:</span> Athlete’s foot, fungus, dry skin, relaxation and general foot health can be served by a footbath. Place 3 or 4 drops of a single oil or blend in very warm water and soak until the water is cool.
        <br>
        <br>

        <span class="ptext-bold">Internal Use:</span> Some practitioners advise against taking any oil internally. Others suggest that essential oils should only be taken under the supervision of a qualified practitioner. I list some here that I am comfortable using internally. Use your own intuition or consult with a practitioner. It is important to use only the highest quality oils for this purpose.
        <br>
        <br>

        <span class="ptext-bold">Massage Therapy:</span> Massaging muscles can relax, release stress, emotions and improve muscle tone; aid in healing muscle, tendon or ligament injuries; and increase circulation of both blood and lymph. Essential oils are a natural complement to massage.
        <br>
        <br>

        <span class="ptext-bold">“Neat:”</span> There are some circumstances where I have found it helpful and very effective to place oil straight on the skin without a carrier oil. This should be done cautiously to avoid skin reactions, especially on children. Never put oil “neat” on the sensitive skin of the face, except the temples where noted.
        <br>
        <br>

        <span class="ptext-bold">Steam method:</span> Place a drop or two of essential oils into a bowl of boiling water and place a towel over the head. Lower the face towards the bowl, close eyes and breathe deeply under the towel tent for several minutes. Break and repeat multiple times until the water is cool. The steam method is helpful for lung issues, sinus infections, runny noses or cold symptoms. Repeat several times a day until clear. The Steam method is also helpful for acne or as a rejuvenating facial.
