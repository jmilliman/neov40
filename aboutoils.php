      <h1 class="header--vertical-margin">Storing Oils</h1>
      <p class="ptext">
        For long-term storage, keep essential oils and blends in a cool, dark place. Once opened, they have been exposed to oxygen and as the bottle empties, oxygen exposure increases in the empty space inside the bottle. For this reason, we only sell 5 ml bottles (they will retain the most potency).
        <br>
        <br> Most essential oils are good for at least a year once opened. I store oils at about 60 degrees in a dark cabinet. I have oils that I have had for more than two years that have been opened, and their potency is still strong. The exception is citrus oils, which are delicate and begin to lose their potency in 90-120 days after being opened. There are exceptions: Frankincense, Myrrh, Sandalwood, Ylang Ylang, and essential oils that have been extracted through the CO, CO2 or traditionally distilled are more stable and the oils can improve with age. These aromatics become mellower, richer or deeper – when stored properly.
