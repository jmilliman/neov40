      <h1 class="header--vertical-margin">Carrier Oils</h1>

      <p class="ptext">
        Carrier oils are used to distribute the essential oils onto the body and have many helpful properties that combine with the essential oils to increase their abilities. Cold Pressed and Extra Virgin oils are best. Avoid hydrogenated oils as they do not have the health benefits, and they may increase cholesterol and block arteries.
        <br>
        <br> Carrier oils become rancid over time as they are affected by oxygen. Some oils have more stability as they contain tocopherol antioxidants, which slow the process. Carrier oils should be kept in a cool, dry place. To decide which oil is best and what volume to buy, it is helpful to know their uses and general “shelf life.” Below are general descriptions for the most widely used carrier oils.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Avocado Oil</span> is a thick oil, rich in Vitamins A, D, E, lecithin and potassium. It is high in sterolins, which are known to reduce age spots and help heal sun damage and scars. It is great for mature skin and relieves the dryness and itching associated with eczema or psoriasis. To lighten the overall thickness, you can blend it with another carrier oil ( I generally choose 75% Apricot Kernel, Almond, Jojoba or other oil, and up to 25% Avocodo). Avocado Oil has a shelf life of up to 1 year.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Sweet Almond Oil</span> contains Omega 6 fatty acids, is high in Vitamins B, Vitamin E and trace minerals. It is a light oil that does not clog pores, goes on smoothly, and is a good choice for lessening wrinkles, acne and aids digestion. Almond oil is odorless. I favor it in instances where I do not want the added scent of carrier oil. It has a shelf life up to 1 year.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Apricot Kernel Oil</span> contains Vitamins A, C &amp; E, as well as lineolic and aolic acids, a good source of unsaturated fats. It is helpful for acne, eczema and skin disorders. This is light oil that moisturizes and balances skin tone, which makes it one of the most popular oils for facial products. It does not clog pores. It has a shelf life up to 1 year.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Evening primrose Oil</span> is high in essential fats that aid inflammation, which make it helpful for arthritis, acne, eczema and allergies. It has a shorter shelf life of 6-9 months. Add drops of Vitamin E to blends and mix with another carrier oil to extend the shelf life of a blend.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Grapeseed Oil</span> is high in bioflavonoids, antioxidants and lineolic acids. Grapeseed oil is readily absorbed. I often make a carrier mix of 1/3 Grapeseed and 2/3 of another carrier oil to boost the nutrition of the blend and increase the shelf life of the Grapeseed oil. Grapeseed is a more volatile oil, reacting to oxygen more readily and has a shelf life of only 3-6 months.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Hazelnut Oil</span> is a good source of B complex Vitamins, Vitamin E, Vitamin K, potassium, magnesium and calcium -- all of which improve skin tone and circulation. It is a good choice for face and skin care. It is dry oil, so it does not leave an oily residue on the skin. It has a shelf life of up to 1 year.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Jojoba Oil</span> most closely resembles the sebum of the skin. It is readily absorbed and works well in mixtures for skin care, aging skin, and perfumes. It has a high Vitamin E content, which makes it helpful in reducing scars and injuries, and is nourishing to abused skin and hair. It is helpful to those with rosacea. It will not harm the eyes. It does not react readily to oxygen, so it is stable with a long shelf life of 2 or more years. It is a great massage oil for mature skin.
        <br>
        <br>

        <span class="ptext-bold">Cold-Pressed Olive Oil</span> is a very healthy, readily absorbed mono-saturated fat high in Vitamins A &amp; E. It’s soothing to rub on the skin during illness. I also use it as a healthy alternative to mineral oil (a petroleum based product) on babies and young children. Olive oil is economical and a great general carrier oil. Women have used it for thousands of years to mend split ends and give nourishment and shine to hair. It is stable and doesn’t go rancid for up to 2 years when stored properly.
        <br>
        <br>

        <span class="ptext-bold">Rose Hip Seed Oil (Rosa rubiginosa)</span> essential oil is high in both Linoleic fatty acid and Linolenic fatty acids (Vitamin F). The body does not produce these but they are required for good cell health. These benefit the body by reducing scars, wrinkles and nurturing healthy skin cells. It can reduce the size of pores. It is high in Vitamin A, so it is great for acne, pimples and boils. It is also helpful for burns and varicose veins. Rosehip seed oil has a shelf life of 6-9 months.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed Wheat Germ Oil</span> is loaded with Vitamin B, Vitamin E, lineolic and linoleic acids, which are helpful to the heart, healthy cholesterol and tissues. It has octacosanol, helpful to muscle health and strength. I use wheat germ in any blend where I am aiding muscle injury, burns or wounds. It is a thick and somewhat sticky oil, but very stable with a shelf life up to 2 years. I use 10% Wheat Germ and 90% of another carrier oil to minimize that sticky feeling.

