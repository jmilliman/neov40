        <br>

        <span class="ptext-bold">Toxicity:</span> Remember, essential oils are concentrated so any essential oil can become toxic. The “more is better” philosophy is not true. Wintergreen, pennyroyal, savin, calamus, rue, wormseed, mugwort and others contain qualities that can be harmful if used improperly. These should be avoided without the help of a skilled practitioner.
