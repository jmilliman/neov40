    <article class="article--small">   <!-- Extraction Methods -->
      <h1 class="header--vertical-margin">Extraction Methods</h1>
      <p class="ptext">
        The second aspect of importance is how the essential oils are extracted from the plants. Different plants require different methods of distillation in order to retain their best qualities. These methods are:
        <br>
        <br>

        <span class="ptext-bold">Steam distillation:</span> Plant qualities degrade when exposed to high heat. Steam distillation is an effective extraction method when plants readily gift their qualities.
        <br>
        <br>

        <span class="ptext-bold">CO2:</span> Plants thrive on carbon dioxide (CO2). When pressure is applied to CO2 it becomes a liquid and plant materials bind to it, without being degraded by high temperatures. More of the plant is saved when using CO2. These oils are thicker and can set up over time. If the oil does not pour easily, place the bottle of oil in a glass of warm water for a few minutes. As it warms, the oil will pour easily. Keep the water below the lid so it does not contaminate the bottle.
        <br>
        <br>

        <span class="ptext-bold">CO2 Select:</span> Some essentials oils are obtained at higher CO2 pressures and contain all CO2 soluble components, including waxes, resins, and plant dyes. These oils most closely resemble the plant itself. CO2 oils are also thick and benefit from using the warming method described under CO2 to simplify pouring.
        <br>
        <br>

        <span class="ptext-bold">Cold Pressed:</span> Lemon, orange and lime essential oils are largely contained in the peel. The Cold pressed method uses stainless steel plates to press the oils out of the peel without using heat on these more delicate oils.
        <br>
        <br>

        <span class="ptext-bold">Extraction:</span> Is used on flowers where high heat is undesirable, yet there is a desire to keep more of the heavier notes of the scent. The flower is put in a solvent, gently heated electrically and then filtered, making a paste. The wax is removed and then transferred to alcohol, which is burned off again at very low heat. Rose, Tuberose and Jasmine are examples of this type of extraction.
