        Here are some of the words the main words in herbalism to describe the affects of plants and in this instance, the oils that come from them. Not sure which oil will help you? You can use this key word in your search and it will take you to the oils that are helpful to these conditions.
        <br>
        <br>

        <span class="ptext-bold">Alteratives:</span>&nbsp; Blood purifiers that aid in treating toxicity of the blood, infections, arthritis, or skin eruptions.
        <br>
        <br>

        <span class="ptext-bold">Analgesics:</span>&nbsp; Reduce pain by relieving muscle spasms, reduces inflammation or affecting nerves that carry pain impulses.
        <br>
        <br>

        <span class="ptext-bold">Antacids:</span>&nbsp; Neutralize stomach acid or counter the effects of excess acid in the gastrointestinal tract.
        <br>
        <br>

        <span class="ptext-bold">Anti-asthmatics:</span>&nbsp; Benefit the conditions of asthma.
        <br>
        <br>

        <span class="ptext-bold">Antibiotics:</span>&nbsp; Inhibit the growth and replication of bacteria, viruses or amoebas. Plants are stationary and cannot avoid exposure to infectious agents. They have defense mechanisms – often essential oils – that ward off pathogens.
        <br>
        <br>

        <span class="ptext-bold">Anticatarrhal:</span>&nbsp; Catarrh means mucous or phlegm. Anti-catarrhals remove or decrease the production of excess mucous or phlegm.
        <br>
        <br>

        <span class="ptext-bold">Antipyretics:</span>&nbsp; “Pyresis” (meaning “fire”) refers to the fever that accompanies most infectious processes (colds, bacterial infections, etc.) and many inflammatory disorders (e.g., lupus or rheumatoid arthritis). Anti-pyretics prevent or reduce fevers or are generally cooling in character.
        <br>
        <br>

        <span class="ptext-bold">Antiseptics:</span>&nbsp; Much like antibiotics, antiseptics prevent the replication of bacteria, fungi and viruses. The term antiseptic is usually applied to an agent that is applied externally to prevent bacterial growth.
        <br>
        <br>

        <span class="ptext-bold">Antispasmodics:</span>&nbsp; Herbs that reduce or prevent muscle spasms, whether they’re in skeletal muscle or in the smooth muscles of the body such as the GI tract, airways, urogenital tract, etc.
        <br>
        <br>

        <span class="ptext-bold">Astringents:</span>&nbsp; Any agent that tends to shrink or constrict living tissue. This is a particularly important herbal property, as it has so many applications in human illness. Astringents slow bleeding, reduce swelling and decrease secretions. For the most part, herbs’ astringent properties stem from tannins, which are found in most plants but are particularly concentrated in barks, roots and nuts.
        <br>
        <br>

        <span class="ptext-bold">Carminatives:</span>&nbsp; Reduce intestinal gas, bloating and cramping.
        <br>
        <br>

        <span class="ptext-bold">Cholagogue:</span>&nbsp; “chole” is a Greek root for bile or gall. Cholagogues are substances that promote the movement of bile from the gallbladder to the small intestine.
        <br>
        <br>

        <span class="ptext-bold">Demulcents:</span>&nbsp; Plants that soothe or protect inflamed mucous membranes, usually by coating them with mucilage. It is helpful to use a demulcent herb with a diuretic to protect the kidney and urinary tract, especially when kidney stones or gravel are thought to be present.
        <br>
        <br>

        <span class="ptext-bold">Diaphoretics:</span>&nbsp; Sweat-inducing plants that can be invaluable during illnesses that include fever. When using a sweating tea it should be hot. When cold, they act as a diuretic.
        <br>
        <br>

        <span class="ptext-bold">Diuretics:</span>&nbsp; Herbs that are used to promote the flow of urine. This is beneficial for people with fluid retention and edema, obesity, bladder infections (which should also be treated with antibiotics) and other conditions where excess fluid is problematic or where increased urinary flow would be useful.
        <br>
        <br>

        <span class="ptext-bold">Emetics:</span>&nbsp; Agents that induce vomiting and empty the stomach. Ipecac is the herbal emetic that is familiar to most people.
        <br>
        <br>

        <span class="ptext-bold">Emmenagogues:</span>&nbsp; Herbs that promote menstrual flow and help regulate irregular cycles. Emmenagogues should not be used during pregnancy or when a woman is trying to conceive.
        <br>
        <br>

        <span class="ptext-bold">Emollients:</span>&nbsp; Compounds that smooth, moisten and soothe the skin. Herbal oils (almond, sesame, apricot, wheat germ, etc.) or plants with high mucilage content make good emollients.
        <br>
        <br>

        <span class="ptext-bold">Expectorants:</span>&nbsp; Herbs that liquefy mucous or promote its expulsion from the respiratory passages. Some of these herbs loosen mucous by virtue of their high mucilage content, while others stimulate the cilia that propel mucous along the airways.
        <br>
        <br>

        <span class="ptext-bold">Galactogogues:</span>&nbsp; Herbs that stimulate milk secretion.
        <br>
        <br>

        <span class="ptext-bold">Hemostatics:</span>&nbsp; Any agent that slows or arrests active bleeding. Most hemostatics are potent astringents that shrink blood vessel walls, but they might also directly affect the coagulation process by acting on platelets or clotting proteins.
        <br>
        <br>

        <span class="ptext-bold">Laxatives:</span>&nbsp; Stimulate bowel movements.
        <br>
        <br>

        <span class="ptext-bold">Lithotriptics:</span>&nbsp; Herbs that help dissolve urinary and gallbladder stones or “gravel.”
        <br>
        <br>

        <span class="ptext-bold">Nervines:</span>&nbsp; Agents that reduce tension and anxiety and promote healthy nervous system function.
        <br>
        <br>

        <span class="ptext-bold">Parasiticides:</span>&nbsp; Herbs that help eliminate parasites from the gastrointestinal tract or skin.
        <br>
        <br>

